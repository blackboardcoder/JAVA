package Control_Statement;

public class IF_ELSE_IF_LADDER {

	public static void main(String[] args) {
		//A common programming construct that is based upon a sequence of nested if's is	the if-else-if ladder.
		
		int a=20;
		if(a>30)
			System.out.println("TRUE a>30");
		else if(a>20)
			System.out.println("TRUE a>20");
		else if(a>10)
			System.out.println("TRUE a>10");
		else
			System.out.println("ALL STATEMENTS ARE FALSE");
	}

}

