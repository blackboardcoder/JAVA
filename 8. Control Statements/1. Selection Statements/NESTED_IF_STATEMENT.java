package Control_Statement;

public class NESTED_IF_STATEMENT {

	public static void main(String[] args) {
		int a=10;
		int b=22;
		//Nested If else means another if statement within another if statement
		if(a==10)//if Statement is true then true block will be executed
		{
			if(b==22)
			{
				System.out.println("TRUE");
			}
		}
		else
		{
			System.out.println("FALSE");
		}

	}

}

