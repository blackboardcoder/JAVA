package Data_Types;

public class Primitive_Types {

	public static void main(String[] args) {
		
		//Integers Type
		byte  b = 10;
		short s = 20;
		int   i = 30;
		long  l = 123456;
		
		System.out.println("****Integers Type****");
		System.out.println("Byte Type Value: " +b);
		System.out.println("Short Type Value: " +s);
		System.out.println("Integer Type Value: " +i);
		System.out.println("Long Type Value: " +l);
		
		//Floating-Point Type
		float  f = 1.23f; //f should be passed as suffix to the value
		double d = 2.23456;
		System.out.println("****Floating-Point Type****");
		System.out.println("Float Type Value: " +f);
		System.out.println("Double Type Value: " +d);
		
		//Character Type
		char c = 'A';
		System.out.println("****Character Type****");
		System.out.println("Character Type Value: " +c);
		
		//Boolean Type
		boolean bl =false;
		System.out.println("****Boolean Type****");
		System.out.println("Boolean Type Value: "+bl);
	}

}


